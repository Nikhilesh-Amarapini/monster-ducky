sprites.onOverlap(SpriteKind.Player, SpriteKind.Food, function (sprite, otherSprite) {
    for (let index = 0; index < 1; index++) {
        pause(5000)
        if (controller.B.isPressed()) {
            controller.moveSprite(mySprite, Speed + 50, Speed + 50)
            mySprite3.setPosition(randint(Math.min(100, 1000), 100 + 1000), randint(Math.min(100, 1000), 100 + 1000))
        }
        if (controller.A.isPressed()) {
            controller.moveSprite(mySprite, Speed + 25, Speed + 25)
            mySprite3.setPosition(randint(Math.min(100, 1000), 100 + 1000), randint(Math.min(100, 1000), 100 + 1000))
            game.gameOver(true)
        }
    }
    controller.moveSprite(mySprite, Speed + 50, Speed + 50)
    mySprite3.setPosition(randint(Math.min(100, 1000), 100 + 1000), randint(Math.min(100, 1000), 100 + 1000))
    game.gameOver(true)
})
sprites.onOverlap(SpriteKind.Player, SpriteKind.Enemy, function (sprite, otherSprite) {
    game.gameOver(false)
    pause(500)
    game.splash(game.runtime())
    for (let index = 0; index < 1; index++) {
        Time = 0
        if (controller.A.isPressed()) {
            Speed = 200
            mySprite = sprites.create(assets.image`myImage`, SpriteKind.Player)
            controller.moveSprite(mySprite, Speed, Speed)
            mySprite2.follow(mySprite)
            mySprite2 = sprites.create(assets.image`myImage0`, SpriteKind.Enemy)
            mySprite2.setPosition(randint(100, 1000), randint(100, 1000))
        } else if (controller.B.isPressed()) {
            Speed = 250
            mySprite = sprites.create(assets.image`myImage`, SpriteKind.Player)
            controller.moveSprite(mySprite, Speed, Speed)
            mySprite2 = sprites.create(assets.image`myImage0`, SpriteKind.Enemy)
            mySprite2.setPosition(0, 1)
            mySprite2.follow(mySprite)
            mySprite3 = sprites.create(assets.image`myImage1`, SpriteKind.Food)
            mySprite3.setPosition(randint(100, 200), randint(100, 200))
        } else {
            Speed = 250
            mySprite = sprites.create(assets.image`myImage`, SpriteKind.Player)
            controller.moveSprite(mySprite, Speed, Speed)
            mySprite2 = sprites.create(assets.image`myImage0`, SpriteKind.Enemy)
            mySprite2.setPosition(0, 1)
            mySprite2.follow(mySprite)
            mySprite3 = sprites.create(assets.image`myImage1`, SpriteKind.Food)
            mySprite3.setPosition(randint(100, 200), randint(100, 200))
        }
    }
})
